//wap to find the factorial of given number by taking user input.

import java.util.Scanner;
class  fact
{
	void factorial(int fact)
	{
		for (int i=1;i<=5 ; i++)
		{
			fact=fact*i;
			//System.out.println(fact);
		}
		System.out.println(fact);
	}
	public static void main(String[] args) 
	{
		Scanner sc1=new Scanner(System.in);
		fact s1=new fact();
		System.out.println("enter the value fact");
		int a1=sc1.nextInt();
		s1.factorial(a1);
		

	}
}
